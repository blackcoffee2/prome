/*
 * Panda sprite — pixel-art data and canvas compositor.
 *
 * The buddy is drawn procedurally rather than from pre-baked image frames. The
 * panda is described as small grids of palette indices: one constant BODY grid
 * (the same on every frame) plus a handful of mutable OVERLAY parts (eyes,
 * arms, props) that a gesture swaps in and out. A gesture is therefore data —
 * a selection of which overlay grids to draw and a vertical offset — not an
 * additional image asset, so the whole animation set costs no flash beyond
 * these tables and adds no PNG-conversion build step.
 *
 * This file is kept separate from the app logic (app_buddy.c) so the art can be
 * edited in isolation: tweaking the panda's shape, palette, or a gesture pose
 * never touches the state machine, and vice versa.
 *
 * Arms model (important). The arms are NOT part of the body grid. The body
 * contains only the parts that never move — ears, head, face, torso, and feet —
 * and the arms are always supplied by exactly one ARMS overlay drawn over it.
 * ARMS_REST draws the default down-paws at the body's sides; a gesture swaps in
 * an alternative arms grid (wave, both-up, peek) that REPLACES the resting arms
 * rather than adding a second pair. Because only one arms overlay is ever drawn
 * at a time, the panda has a single set of arms that genuinely changes pose
 * between frames, instead of a raised paw appearing alongside a resting one.
 * Every arms grid therefore includes the whole arm from its attachment at the
 * torso out to the paw, so the limb stays connected to the body in every pose.
 *
 * Grid model. Every part is a PANDA_GRID_H x PANDA_GRID_W array of uint8_t
 * palette indices. PANDA_TRANSPARENT is the reserved index meaning "draw
 * nothing here" so overlays composite over the body and the body composites
 * over whatever the canvas already holds. Authoring is in this small logical
 * space (32x32); the compositor scales each logical pixel up to a PANDA_SCALE
 * square block when it blits, so the result reads as crisp, chunky pixel art on
 * the panel rather than a tiny 32px image.
 *
 * Palette. A compact set of named colors (PANDA_PALETTE), indexed by the small
 * integers the grids contain. The panda's own colors are app-private and live
 * here; only the prop accent is sourced from the device theme (THEME_ACCENT)
 * so a re-theme carries the buddy's hearts and sparkles with it. Because the
 * accent is not a compile-time constant expression, the palette's accent slot
 * is filled at runtime in panda_sprite_init rather than in the static
 * initializer.
 *
 * Compositor. panda_sprite_render clears the canvas, then blits the body and a
 * caller-chosen set of overlays at a caller-chosen vertical offset. It writes
 * straight into the canvas draw buffer honoring its row stride (LVGL aligns
 * each canvas row, so a row base is stride * y, not packed width * 2) — the
 * same stride-aware discipline the camera blit uses. It paints only the
 * panda's scaled bounding box plus the cleared margin, and the app invalidates
 * just that region, so the partial-buffer SPI panel repaints a small stable
 * rectangle rather than the whole screen.
 */
