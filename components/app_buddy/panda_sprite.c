/*
 * Blit one logical pixel as a PANDA_SCALE square block into the canvas draw
 * buffer at logical position (gx, gy) shifted by the pixel-space (ox, oy)
 * origin. Writes honor the draw buffer's row stride rather than assuming a
 * packed width*2 layout, matching the camera blit. Blocks that fall partly
 * outside the canvas are clipped per-row/column so an offset pose near an edge
 * cannot write out of bounds.
 */
static inline void blit_block(lv_draw_buf_t *db, int32_t gx, int32_t gy,
                              int32_t ox, int32_t oy, lv_color_t color)
{
    const int32_t stride = (int32_t)db->header.stride;
    const int32_t cw = (int32_t)db->header.w;
    const int32_t ch = (int32_t)db->header.h;
    uint8_t *base = db->data;

    const uint16_t packed = lv_color_to_u16(color);

    int32_t px0 = ox + gx * PANDA_SCALE;
    int32_t py0 = oy + gy * PANDA_SCALE;

    for (int32_t dy = 0; dy < PANDA_SCALE; dy++) {
        int32_t py = py0 + dy;
        if (py < 0 || py >= ch) {
            continue;
        }
        uint16_t *row = (uint16_t *)(base + stride * py);
        for (int32_t dx = 0; dx < PANDA_SCALE; dx++) {
            int32_t px = px0 + dx;
            if (px < 0 || px >= cw) {
                continue;
            }
            row[px] = packed;
        }
    }
}

/*
 * Blit a whole grid: for every non-transparent cell, draw its palette color as
 * a scaled block. Shared by the body and every overlay; the only difference
 * between them is the grid passed in and the order of calls (body first, then
 * overlays over it).
 */
static void blit_grid(lv_draw_buf_t *db, const uint8_t (*grid)[PANDA_GRID_W],
                      int32_t ox, int32_t oy)
{
    for (int32_t gy = 0; gy < PANDA_GRID_H; gy++) {
        for (int32_t gx = 0; gx < PANDA_GRID_W; gx++) {
            uint8_t idx = grid[gy][gx];
            if (idx == PANDA_COL_TRANSPARENT) {
                continue;
            }
            blit_block(db, gx, gy, ox, oy, s_palette[idx]);
        }
    }
}

void panda_sprite_render(lv_obj_t *canvas, const panda_pose_t *pose)
{
    if (canvas == NULL || pose == NULL) {
        return;
    }
    lv_draw_buf_t *db = lv_canvas_get_draw_buf(canvas);
    if (db == NULL || db->data == NULL) {
        return;
    }

    /* Clear to the app background each frame. The buddy app fills the canvas
     * with its backdrop color; clearing to the same color erases the previous
     * pose cleanly before the new one is composited. */
    lv_canvas_fill_bg(canvas, pose->bg, LV_OPA_COVER);

    /* Center the 32x32 grid (scaled) horizontally in the canvas, and place it
     * with a small top margin so a downward bob still leaves headroom. The
     * gesture's y_offset shifts the whole panda vertically for bobs and hops
     * (negative is up). */
    const int32_t sprite_w = PANDA_GRID_W * PANDA_SCALE;
    const int32_t cw = (int32_t)db->header.w;
    const int32_t ox = (cw - sprite_w) / 2;
    const int32_t oy = PANDA_TOP_MARGIN_PX + pose->y_offset;

    /* Body first, then arms over it, then eyes, then any prop on top. Arms are
     * drawn before eyes so a peek pose's covering paws sit over the face. A
     * NULL eyes/prop entry is skipped; the arms entry is always a real grid
     * (ARMS_REST by default), since the body carries no arms of its own. */
    blit_grid(db, PANDA_BODY, ox, oy);

    if (pose->arms < PANDA_ARMS_COUNT && ARMS_TABLE[pose->arms] != NULL) {
        blit_grid(db, ARMS_TABLE[pose->arms], ox, oy);
    }
    if (pose->eyes < PANDA_EYES_COUNT && EYES_TABLE[pose->eyes] != NULL) {
        blit_grid(db, EYES_TABLE[pose->eyes], ox, oy);
    }
    if (pose->prop < PANDA_PROP_COUNT && PROP_TABLE[pose->prop] != NULL) {
        blit_grid(db, PROP_TABLE[pose->prop], ox, oy);
    }
}